[ORG 0x7c00]
    xor ax, ax
    mov ds, ax
    mov si, msg
    
call Bios_Print

;   Main
;   ----------------------------------------------------------------------------------------------------
Wait_Command:

jmp Wait_Command

;   Print Function
;   ----------------------------------------------------------------------------------------------------
Bios_Print:
    lodsb
    or al, al
    jz Call_Done
    mov ah, 0x0E
    int 0x10
jmp Bios_Print

;   Progress Done Signal
;   ----------------------------------------------------------------------------------------------------
Call_Done:
    ret

;   Keypres
;   ----------------------------------------------------------------------------------------------------
Keypress:
    mov ah, 1
    int 16h    
jz Keypress
    mov ah, 0
    int 16h
ret

;   Define
;   ----------------------------------------------------------------------------------------------------
msg db  'Machine', 13, 10, 0

;   Zero
;   ----------------------------------------------------------------------------------------------------
times 510-($-$$) db 0

;   Sector Done
;   ----------------------------------------------------------------------------------------------------
db 0x55
db 0xAA